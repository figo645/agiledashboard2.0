package com.example.dashboard.entity;

public class TestingData {
    private int week;
    private int plannedTesting;
    private int actualTesting;
    private int teamATesting;
    private int teamBTesting;
    private int teamCTesting;
    private int totalTestCases;
    private int passedTestCases;
    private int failedTestCases;
    private int blockedTestCases;

    // Getters and Setters
    public int getWeek() {
        return week;
    }

    public void setWeek(int week) {
        this.week = week;
    }

    public int getPlannedTesting() {
        return plannedTesting;
    }

    public void setPlannedTesting(int plannedTesting) {
        this.plannedTesting = plannedTesting;
    }

    public int getActualTesting() {
        return actualTesting;
    }

    public void setActualTesting(int actualTesting) {
        this.actualTesting = actualTesting;
    }

    public int getTeamATesting() {
        return teamATesting;
    }

    public void setTeamATesting(int teamATesting) {
        this.teamATesting = teamATesting;
    }

    public int getTeamBTesting() {
        return teamBTesting;
    }

    public void setTeamBTesting(int teamBTesting) {
        this.teamBTesting = teamBTesting;
    }

    public int getTeamCTesting() {
        return teamCTesting;
    }

    public void setTeamCTesting(int teamCTesting) {
        this.teamCTesting = teamCTesting;
    }

    public int getTotalTestCases() {
        return totalTestCases;
    }

    public void setTotalTestCases(int totalTestCases) {
        this.totalTestCases = totalTestCases;
    }

    public int getPassedTestCases() {
        return passedTestCases;
    }

    public void setPassedTestCases(int passedTestCases) {
        this.passedTestCases = passedTestCases;
    }

    public int getFailedTestCases() {
        return failedTestCases;
    }

    public void setFailedTestCases(int failedTestCases) {
        this.failedTestCases = failedTestCases;
    }

    public int getBlockedTestCases() {
        return blockedTestCases;
    }

    public void setBlockedTestCases(int blockedTestCases) {
        this.blockedTestCases = blockedTestCases;
    }
} 